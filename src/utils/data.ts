export function incrementQty(oldQty: string | null | undefined, newQty: string | null | undefined): string {
  const parse = (s: any) => {
    const str = String(s || '').trim();
    if (!str) return { val: 1, unit: '' };
    const num = parseFloat(str);
    if (isNaN(num)) return { val: 1, unit: str };
    const unit = str.replace(String(num), '').trim();
    return { val: num, unit };
  };

  const o = parse(oldQty);
  const n = parse(newQty);

  const total = parseFloat((o.val + n.val).toFixed(2));
  const unit = o.unit || n.unit;
  return unit ? `${total} ${unit}` : String(total);
}

export function mergeItems(existing: Item[], newItems: Item[]): Item[] {
  const merged = [...existing];
  for (const newItem of newItems) {
    const nName = norm(newItem.name);
    const existingIndex = merged.findIndex(i => norm(i.name) === nName);
    if (existingIndex >= 0) {
      const ex = merged[existingIndex];
      merged[existingIndex] = {
        ...ex,
        qty: incrementQty(ex.qty, newItem.qty),
        isChecked: false
      };
    } else {
      merged.push({ ...newItem, id: newItem.id || uid() });
    }
  }
  return merged;
}
export function areUnitsCompatible(u1: string, u2: string): boolean {
  const norm = (u: string) => (u || '').trim().toLowerCase();
  const n1 = norm(u1);
  const n2 = norm(u2);
  
  if (n1 === n2) return true;
  
  const mass = ['г', 'кг'];
  const vol = ['мл', 'л'];
  
  if (mass.includes(n1) && mass.includes(n2)) return true;
  if (vol.includes(n1) && vol.includes(n2)) return true;
  
  return false;
}

export function decrementQty(oldQty: string, subVal: number, subUnit: string): string | null {
  const parse = (s: string) => {
    const str = s.trim();
    const num = parseFloat(str);
    if (isNaN(num)) return { val: 0, unit: str };
    const unit = str.replace(String(num), '').trim();
    return { val: num, unit };
  };

  const o = parse(oldQty);
  const u1 = o.unit.toLowerCase();
  const u2 = subUnit.toLowerCase();

  if (!areUnitsCompatible(u1, u2)) return oldQty;

  // Convert to base units for calculation
  let v1 = o.val;
  let v2 = subVal;

  if (u1 === 'кг') v1 *= 1000;
  if (u1 === 'л') v1 *= 1000;
  
  if (u2 === 'кг') v2 *= 1000;
  if (u2 === 'л') v2 *= 1000;

  const remaining = parseFloat((v1 - v2).toFixed(2));
  if (remaining <= 0) return null;

  // Format back based on original unit or scale
  const baseUnit = ['г', 'кг'].includes(u1) ? 'г' : (['мл', 'л'].includes(u1) ? 'мл' : u1);
  
  if (baseUnit === 'г') {
    if (remaining >= 1000) return `${parseFloat((remaining / 1000).toFixed(2))} кг`;
    return `${Math.round(remaining)} г`;
  }
  if (baseUnit === 'мл') {
    if (remaining >= 1000) return `${parseFloat((remaining / 1000).toFixed(2))} л`;
    return `${Math.round(remaining)} мл`;
  }

  return `${remaining} ${o.unit}`;
}
